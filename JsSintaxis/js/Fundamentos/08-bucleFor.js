'use strict'

/*
  Bucle for
  es una estructura de control que se repite varias veces


  La estructura es:

  var i = 0;  contador
  i <= numero;  condicion
  i++ incrementador

  var i = 0; i <= numero; i++

*/



var numero = 50 ;

for(var i = 0; i <= numero; i++){
    console.log("vamos por el numero: "+i);

}



